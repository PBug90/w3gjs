const playerColor = (color: number): string => {
  switch (color) {
    case 0:
      return '#ff0000'
    case 1:
      return '#0000FF'
    case 2:
      return '#008080'
    case 3:
      return '#800080'
    case 4:
      return '#FFFF00'
    case 5:
      return '#FFA500'
    case 6:
      return '#008000'
    case 7:
      return '#FFC0CB'
    case 8:
      return '#808080'
    case 9:
      return '#ADD8E6'
    case 10:
      return '#006400'
    case 11:
      return '#ADD8E6'
    case 12:
      return '#800000'
    case 13:
      return '#000080'
    case 14:
      return '#40E0D0'
    case 15:
      return '#EE82EE'
    case 16:
      return '#F5DEB3'
    case 17:
      return '#FFDAB9'
    case 18:
      return '#F5FFFA'
    case 19:
      return '#E6E6FA'
    case 20:
      return '#3eb489'
    case 21:
      return '#FFFAFA'
    case 22:
      return '#50c878'
    case 23:
      return '#D0B078'
    default:
      return '000000'
  }
}

const gameVersion = (version: number): string => {
  if (version === 10030){
    return `1.30.2+`
  }
  return `1.${version}`
}

export default {
  playerColor,
  gameVersion
}
